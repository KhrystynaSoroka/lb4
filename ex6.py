a = int(input("Задайте значення першої сторони трикутника: "))
b = int(input("Задайте значення другої сторони трикутника: "))
c = int(input("Задайте значення третьої сторони трикутника: "))

if a==b==c:
    print("Трикутник рівносторонній")
elif a==b or b==c or a==c:
    print("Трикутник рівнобедрений")
elif a+b<c or b+c<a:
    print("Трикутник не існує")
else: print("Трикутник різносторонній")
